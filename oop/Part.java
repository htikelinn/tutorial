public class Part {
    private String name;
    private long number;
    private double root;

    public Part(String na, long nu, double ro) {
        name = na;
        number = nu;
        root = ro;
    }

    public String getName() {
        return name;
    }

    public long getnumber() {
        return number;
    }

    public double getroot() {
        return root;
    }
}
